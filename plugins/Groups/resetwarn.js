import { resetWarn, getWarnCount } from '../../database/config.js';
import { getFakeQuoted } from '../../lib/fakeQuoted.js';
import { resolveTargetJid } from '../../lib/lidResolver.js';

export default {
    name: 'resetwarn',
    alias: ['delwarn', 'clearwarn'],
    description: 'Reset warns for a user',
    run: async (context) => {
        const { client, m, isAdmin, isBotAdmin } = context;
        const fq = getFakeQuoted(m);
        await client.sendMessage(m.chat, { react: { text: 'вҢӣ', key: m.reactKey } });

        if (!m.isGroup) return m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ң Group only.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);
        if (!isAdmin && !isBotAdmin) return m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ң Admin only.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);

        let rawJid = m.quoted?.sender || m.mentionedJid?.[0];
        if (!rawJid) return m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ң Reply or mention the user.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);

        const groupMetadata = await client.groupMetadata(m.chat);
        const participants = groupMetadata.participants;
        const target = resolveTargetJid(rawJid, participants);
        if (!target) return m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ң Couldn't find that person in this group.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);

        const userNum = target.split('@')[0].split(':')[0];
        await resetWarn(m.chat, userNum);
        await client.sendMessage(m.chat, { react: { text: 'вң…', key: m.reactKey } });

        return client.sendMessage(m.chat, {
            text: `в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ң Warns cleared for @${userNum} рҹ§№\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`,
            mentions: [target]
        }, { quoted: fq });
    }
};
