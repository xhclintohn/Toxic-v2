const fs = require('fs');
const { getFakeQuoted } = require('../../lib/fakeQuoted');

module.exports = async (context) => {
    const { client, m, participants } = context;
    const fq = getFakeQuoted(m);

    if (!m.isGroup) {
        return m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ңв”Җв”Җв”Җвү« EКҖКҖбҙҸКҖ вүӘв”Җв”Җв”Җ\nв”ң \nв”ң Command meant for groups.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);
    }

    try {
        const gcdata = await client.groupMetadata(m.chat);
        const vcard = gcdata.participants
            .map((a, i) => {
                const number = a.id.split('@')[0];
                return `BEGIN:VCARD\nVERSION:3.0\nFN:[${i}] +${number}\nTEL;type=CELL;type=VOICE;waid=${number}:+${number}\nEND:VCARD`;
            })
            .join('\n');

        const cont = './contacts.vcf';

        await m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ңв”Җв”Җв”Җвү« VCF вүӘв”Җв”Җв”Җ\nв”ң \nв”ң A moment, Toxic-MD is compiling\nв”ң ${gcdata.participants.length} contacts into a VCF...\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);

        await fs.promises.writeFile(cont, vcard);
        await m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ңв”Җв”Җв”Җвү« VCF вүӘв”Җв”Җв”Җ\nв”ң \nв”ң Import this VCF in a separate\nв”ң email account to avoid messing\nв”ң with your contacts...\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);

        await client.sendMessage(
            m.chat,
            {
                document: fs.readFileSync(cont),
                mimetype: 'text/vcard',
                fileName: 'Group contacts.vcf',
                caption: `в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ңв”Җв”Җв”Җвү« VCF вүӘв”Җв”Җв”Җ\nв”ң \nв”ң VCF for ${gcdata.subject}\nв”ң ${gcdata.participants.length} contacts\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`
            },
            { ephemeralExpiration: 86400, quoted: fq }
        );

        await fs.promises.unlink(cont);
    } catch (error) {
        console.error(`VCF error: ${error.message}`);
        await m.reply(`в•ӯв”Җв”Җв”Җ(    TOXIC-MD    )в”Җв”Җв”Җ\nв”ңв”Җв”Җв”Җвү« EКҖКҖбҙҸКҖ вүӘв”Җв”Җв”Җ\nв”ң \nв”ң Failed to generate VCF.\nв”ң Try again later.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җвҳү\n> В©рқҗҸрқҗЁрқҗ°рқҗһрқҗ«рқҗһрқҗқ рқҗҒрқҗІ рқҗұрқҗЎ_рқҗңрқҗҘрқҗўрқҗ§рқҗӯрқҗЁрқҗ§`);
    }
};
